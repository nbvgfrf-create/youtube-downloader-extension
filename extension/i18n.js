function normalizeLocale(locale) {
  if (!locale) {
    return "ru";
  }
  return Object.prototype.hasOwnProperty.call(YTD_LOCALES, locale) ? locale : "ru";
}

function formatMessage(locale, key, vars = {}) {
  const table = YTD_LOCALES[normalizeLocale(locale)] || YTD_LOCALES.ru;
  const fallback = YTD_LOCALES.ru;
  let template = table[key] || fallback[key] || key;
  for (const [name, value] of Object.entries(vars)) {
    template = template.replaceAll(`{${name}}`, String(value));
  }
  return template;
}

